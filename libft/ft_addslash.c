#include "libft.h"

char	*ft_addslash(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (NULL);
	if (s1[ft_strlen(s1) - 1] && s1[ft_strlen(s1) - 1] != '/')
		s1 = ft_stradd(s1, "/");
	s1 = ft_stradd(s1, s2);
	return (s1);
}
