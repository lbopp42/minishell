#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	nb;
	int				result;

	nb = n;
	if (n < 0)
	{
		write(1, "-", 1);
		nb = -n;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	result = (nb % 10) + '0';
	write(1, &result, 1);
}
