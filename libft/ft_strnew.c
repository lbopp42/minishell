#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *s;

	s = (char*)malloc(sizeof(char) * (size + 1));
	if (s && size < 2147483647)
	{
		ft_bzero(s, size + 1);
		return (s);
	}
	return (0);
}
